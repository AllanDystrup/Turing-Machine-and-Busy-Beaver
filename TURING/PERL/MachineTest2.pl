################################################################################
#                       Check TM postcondition
################################################################################
sub check {
   my ($TM, $exp) = @_;
   my $ones = grep (/1/, split(//, $TM->tape()));
   my $res = 'TM ' . $TM->id() . ($exp eq $ones ? ' OK' : ' ***ERROR***');
   $res .= '(' . $TM->get_hTrans() .'-'. $TM->get_hDelta() .'): ';
   $res .= " Expect:$exp, Got:" . $ones; 
   print $res, "\n";
}


